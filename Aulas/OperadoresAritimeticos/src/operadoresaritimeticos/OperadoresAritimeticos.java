
package operadoresaritimeticos;

public class OperadoresAritimeticos {

    public static void main(String[] args) {
        
        int num1= 10;
        
        int num2= ++num1;
        int num3= num1--;
         
        System.out.println("Num1: "+num1);
        System.out.println("Num2: "+num2);
        System.out.println("Num3: "+num3);
        
        
        //Multipilcativo
        
        int ex = 3*10;
        int ex2 = 10/2;
        int ex3 = 5%110;
        
        System.out.println("Multiplicação: "+ex);
        System.out.println("Divisão: "+ex2);
        System.out.println("Porcentagem: "+ex3);
        

        //Aditivo 
        
        int ex4 = 10+2;
        int ex5 = 15-5;
        
        System.out.println("Adição: "+ex4);
        System.out.println("Subtração: "+ex5);

    }
    
    
}
