package tipodedados;

public class TipoDeDados {

    public static void main(String[] args) {
        
        int num1 = 5;
        long num2 = 9;
        short num3 = 60;
        float num4 = 4.5f;
        double num5 = 90.88d;
        char num6 = 'C';
        boolean Java = true;     
        
        System.out.println("Int: "+num1);
        System.out.println("long: "+num2);
        System.out.println("short: "+num3);
        System.out.println("float: "+num4);
        System.out.println("double: "+num5);
        System.out.println("char: "+num6);
        System.out.println("boolean: "+Java);
    }
    
}
